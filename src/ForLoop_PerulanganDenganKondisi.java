public class ForLoop_PerulanganDenganKondisi {
    public static void main(String[] args) {

        //Perulangan Dengan Kondisi
        System.out.println("Perulangan Dengan Kondisi");

        var counter = 1;

        for (; counter <= 10; ) {
            System.out.println("Perulangan " + counter);

            counter++;
        }
    }
}
