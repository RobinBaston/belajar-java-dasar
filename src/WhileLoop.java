public class WhileLoop {
    public static void main(String[] args) {

        //Jika ingin membuat perulangan hanya menggunakan kondisi
        //gunakan While Loop
        //Pengeecekan Kondisi di While Loop,
        //dilakukan di awal seebeelum perulangan dilakukan
        var counter = 1;
        while (counter <= 10){
            System.out.println("Perulangan " + counter);
            counter++;
        }

    }
}
